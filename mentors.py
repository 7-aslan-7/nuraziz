import random

# Приветственное сообщение
print("Добро пожаловать в игру 'Угадай число'!")
print("Компьютер загадал число от 1 до 100. Попробуйте угадать его.")

# Компьютер загадывает случайное число от 1 до 100
secret_number = random.randint(1, 100)
attempts = 0  # Счетчик попыток

# Цикл для угадывания числа
while True:
    # Пользователь вводит число
    guess = int(input("Введите ваше предположение: "))
    attempts += 1  # Увеличиваем счетчик попыток

    # Проверяем, угадал ли пользователь
    if guess == secret_number:
        print(f"Поздравляем! Вы угадали число за {attempts} попыток.")
        break
    elif guess < secret_number:
        print("Загаданное число больше.")
    else:
        print("Загаданное число меньше.")